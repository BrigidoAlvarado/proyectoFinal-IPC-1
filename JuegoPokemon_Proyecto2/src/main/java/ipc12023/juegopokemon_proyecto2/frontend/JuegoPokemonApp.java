    public void refrescar (){
        
        JLblCasillaMapa[][] mapavisible = mapa.getMapaVisible();
        
        jPnlTablero.removeAll();
        jPnlTablero.setBorder(javax.swing.BorderFactory.createTitledBorder(null, mapa.getNombre(), javax.swing.border.TitledBorder.DEFAULT_JUSTIFICATION, javax.swing.border.TitledBorder.DEFAULT_POSITION, new java.awt.Font("sansserif", 1, 12), new java.awt.Color(0, 0, 0))); // NOI18N
        jPnlTablero.setBackground(new java.awt.Color(204, 255, 204));

        for (int i = 0; i < mapavisible.length; i++) {
            for (int j = 0; j < mapavisible.length; j++) {
                
               jPnlTablero.add(mapavisible[i][j]);   
            }    
        }
        
        jPnlTablero.repaint();
        jPnlTablero.revalidate();
       
    }
    
